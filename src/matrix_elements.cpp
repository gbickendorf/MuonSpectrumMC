      (cos(ph2 - ph3) * pow(1 - pow(ct2, 2), 0.5) * pow(1 - pow(ct3, 2), 0.5) *
           pow(pow(E2, 2) - pow(m2, 2), 0.5) -
       (1 + ct3) * (E2 - 2 * m - ct2 * pow(pow(E2, 2) - pow(m2, 2), 0.5))) *
      pow(2 * E1 * E2 + pow(m2, 2) -
              2 * ct1 * ct2 * pow(pow(E2, 2) - pow(m2, 2), 0.5) *
                  pow(pow(E1, 2) - pow(me, 2), 0.5) -
              2 * pow(1 - pow(ct1, 2), 0.5) * pow(1 - pow(ct2, 2), 0.5) *
                  pow(pow(E2, 2) - pow(m2, 2), 0.5) *
                  pow(pow(E1, 2) - pow(me, 2), 0.5) * sin(ph2),
          -1) *
      (-3 * E1 * E2 - 2 * E1 * E3 - E2 * E3 + 2 * E1 * m + E2 * m - pow(m2, 2) -
       2 * pow(me, 2) + ct2 * ct3 * E3 * pow(pow(E2, 2) - pow(m2, 2), 0.5) +
       E3 * cos(ph2 - ph3) * pow(1 - pow(ct2, 2), 0.5) *
           pow(1 - pow(ct3, 2), 0.5) * pow(pow(E2, 2) - pow(m2, 2), 0.5) +
       2 * ct1 * ct3 * E3 * pow(pow(E1, 2) - pow(me, 2), 0.5) +
       3 * ct1 * ct2 * pow(pow(E2, 2) - pow(m2, 2), 0.5) *
           pow(pow(E1, 2) - pow(me, 2), 0.5) +
       3 * pow(1 - pow(ct1, 2), 0.5) * pow(1 - pow(ct2, 2), 0.5) *
           pow(pow(E2, 2) - pow(m2, 2), 0.5) *
           pow(pow(E1, 2) - pow(me, 2), 0.5) * sin(ph2) +
       2 * E3 * pow(1 - pow(ct1, 2), 0.5) * pow(1 - pow(ct3, 2), 0.5) *
           pow(pow(E1, 2) - pow(me, 2), 0.5) * sin(ph3));
  return matel1 + matel2 * m * m / me / me + 2.0 * matel3 * m / me;
